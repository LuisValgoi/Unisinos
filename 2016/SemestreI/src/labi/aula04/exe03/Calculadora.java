package labi.aula04.exe03;


public class Calculadora {
	
	public int soma(int n1, int n2){
		return n1 + n2;
	}
	
	public double soma(double n1, double n2){
		return n1 + n2;
	}
	
	public int subtrai(int n1, int n2){
		return n1 - n2;
	}
	
	public double subtrai(double n1, double n2){
		return n1 - n2;
	}
	
	public int multiplica(int n1, int n2){
		return n1 * n2;
	}

	public double multiplica(double n1, double n2){
		return n1 * n2;
	}

	public int divideReal(int n1, int n2){
		return n1 / n2;
	}
	
	public double divideReal(double n1, double n2){
		return n1 / n2;
	}
	
	public int divideInteira(int n1, int n2){
		return n1 / n2;
	}
	
	public double divideResto(double n1, double n2){
		return n1 % n2;
	}
}
